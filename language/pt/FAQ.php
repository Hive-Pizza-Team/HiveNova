$LNG['questions'][2][3]['body']		= <<<BODY
<p>Uma lua pode formar-se quando um campo de destroços tem pelo menos 100.000 unidades. A probabilidade depende da sorte. A configuração padrão do universo é 1% de hipótese para uma lua. As hipóteses máximas são, no entanto, apenas 20%, independentemente do tamanho do campo de destroços.</p>
<h3>Informações Gerais sobre Luas</h3>
<p>As luas podem ter nomes, e o nome padrão é "Lua". Podes ter mais de uma lua, mas apenas uma por planeta.</p>
<h3>Tamanhos de Luas</h3>
<p>Uma lua pode ser criada dependendo do número de unidades destruídas em combate. Destruir 1667 Cargueiros Ligeiros/Caças tem 20% de hipótese de criar uma lua. Os cargueiros ligeiros são recomendados porque são facilmente destruídos.</p>
<p>O planeta-alvo deverá ter defesas suficientes para destruir as naves. Constrói pelo menos 3334 Lançadores de Mísseis ou Torretas Laser Ligeiras.</p>
<p>A quantidade de destruição da batalha determinará o tamanho da lua.</p>
<p>A probabilidade para Luas entre 1% e 19% é aleatória, enquanto que 20% de hipótese significa que a lua terá um diâmetro médio de 8000 km. As luas normais nunca excedem 8944 km de diâmetro.</p>
<h3>Construção</h3>
<p>Nas luas, podes construir armazéns de recursos, uma Fábrica de Robots ou Nanites, uma Base Lunar, um Sensor Phalanx, um Depósito de Aliança, um Estaleiro e um Portal de Salto Quântico. O Estaleiro não é muito útil porque podes transferir rapidamente naves do planeta. O Depósito de Aliança ajuda a defender a Lua.</p>
<p>Os três edifícios específicos da lua são:</p>
<ul><li><p><b>Base Lunar</b><br>Usada para criar novos campos, e cada nível adiciona 3 campos adicionais.</p></li>
<li><p><b>Sensor Phalanx</b><br>Usado para ver as atividades das frotas inimigas de e para planetas, mas não consegue escanear luas. Tem um alcance limitado de galáxias, mas pode ser melhorado para aumentar o alcance.</p></li>
<li><p><b>Portal de Salto Quântico</b><br>Usado para teleportar uma frota de uma lua para outra instantaneamente. Não pode ser usado para transferir recursos.</p></li></ul>

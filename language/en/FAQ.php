<h3>Inactive Raids:</h3>
<p>A focused raider tries to steal resources fast and easy. Often, your neighbors are all active players who are online or fleet saving. Inactive players are easier targets as they are not likely to come online.</p>
<h3>Alliance Combat System (ACS):</h3>
<p>Alliance Combat System (or ACS) allows for friends and alliance members to temporarily combine their fleets for attack or defence, to take down or repel a more powerful enemy. Building an Alliance Depot is not a pre-requisite to use ACS.</p>
<h3>Intercepting a fleet:</h3>
<p>Fleet interception is difficult. You must determine how, when and where the player is saving the fleet. To do that you have to observe the player. Using phalanx sensor, you can detect fleets on planets. Depending on how the player hides, you may have to destroy moon(s) to detect the fleets.</p>

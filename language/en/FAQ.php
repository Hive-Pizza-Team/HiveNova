<p>A moon can form when a field of debris has at least 100,000 units, and you are lucky with a roll of dice. Default universe setting is a 1% chance for moon. Maxsimum is only 20% chance no matter how big the debris field is.</p>
<h3>General</h3>
<p>The moons can be named and the default name is "Moon". You can have more than one moon, max one per planet. </p>
<h3>Sizes of Moons</h3>
<p>A moon can be spawned depending on the number of units destroyed in combat. Destroying 1667 Light Cargos/Fighters has a 20% chance to spawn a moon. Light Cargos are recommended because they are easy to destroy.</p>
<p>The target planet should have defenses sufficient to destroy the ships. Build at least 3334 Missle Launchers or Light Laser Turrets.</p>
<p>The amount of destruction from the battle will determine the size of the moon</p>
<p>Probability for Moons between 1% and 19% is of random size and probability of 20% the moon will have 8000 km in diameter. Regular moons are never exceeding 8944 km in diameter.</p>
<h3>Construction</h3>
<p>On moons, you can build resource storage, Robot or Nanite Factory, Moonbase, Phalanx Sensor, Alliance Depot, Shipyard and Jumpgate. The Shipyard is not very useful because you can quickly transfer ships from the planet. Alliance Depot is useful for defending the Moon.</p>
<p>The three specific buildings on the moon are:</p>
<ul><li><p><b>Lunar Base</b><br>Used to create new fields, and each level adds 3 additional fields.</p></li>
<li><p><b>Phalanx Sensor</b><br>Used to view activities of enemy fleets to and from planets, but cannot scan moons. It has a limited range of galaxies, but can be upgraded to extend the range.</p></li>
<li><p><b>Jumpgates</b><br>Used to teleport a fleet from one moon to another instantaneously. It cannot be used to transfer resources.</p></li></ul>
<p>Here is a list of the ideal development of a Moon:<br>
<b>Legend:</b><br>
MB – Moon Base<br>
SP – Phalanx<br>
ST – Jumpgate<br>
RB – Robots Factory</p>
<p>MB1 – RB1 – RB2 – MB2 – SP1 – RB3 – MB3 – SP2 – RB4 – MB4 – SP3 – RB5 – MB5 – SP4 – RB6 – MB6 – SP5 – SP6 – MB7 – SP7 – SP8 – MB8 – ST1<br>
This order is only suggested if you are new, because of course an expert player has their own ideas.<p>
<h3>Destruction of Moons:</h3>
<p>Moons can be destroyed by one type of ship, called Battle Fortress.</p>
<p>In the Fleet menu, select a Battle Fortress and target a Moon, then choose Destroy for the mission type. Destroy missions are not guaranteed to succeed. The larger the moon, the more Battle Fortresses required.</p>
